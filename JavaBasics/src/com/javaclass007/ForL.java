package com.javaclass007;

public class ForL {

	public static void main(String[] args) {
		
		//Print odd numbers between 20 and 50 (2 ways)
       
	
		//4/1
		for(int a=20; a<=50;a++) {
		if(a%2!=0) {
			System.out.print(a+" ");
		}
	}
	//4/2
	for(int b=21; b<=50; b+=2) {
		System.out.println(b+" ");
	}
	
	   }
	}


