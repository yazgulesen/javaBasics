package com.javaclass007;

public class DoWhileLoop {

	public static void main(String[] args) {

		
		int num=10;
		
		while(num<=3) {
			System.out.print("hello ");
			num++;
		}
		int num1=10;
		do {
			System.out.println("hello ");
			num1++;
		}while(num1<=3);
		
		
		
		
		int a=1;
		do {
			System.out.print(a+" ");
			a++;
			
		}while(a<=30);
		
		
	
	
	
	
	
	}

}
