package com.javaclass007;

public class ForLoop {

	public static void main(String[] args) {

		
		
		for(int i=1; i<=5; i++) {
			System.out.println("Hello"+" ");
		}
		
		System.out.println("-------I need to print numbers 1 to 20");
		
		for(int b=10; b<=25; b++) {
			System.out.print(b+" ");
		}
		for(int b=50; b>=1; b--) {
			System.out.print(b+" ");
		}
		
		
		
		
	}

}
